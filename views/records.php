                        <div class="bs-blog-naz">
                            <h2>Caracter de los fondos documentales del archivo regional</h2>
                            <div class="bs-blog-text" style="text-align: justify;"> 
                                <p>El Archivo Histórico Regional de Boyacá, se formó de la integración de diferentes Fondos Documentales de distinta procedencia, creado por voluntad de la Academia Boyacense de Historia y de las demás instituciones que quisieron apoyar la obra. Esto indica el carácter distintivo en relación con los demás Archivos del país.</p>
                                <p>De todos los Fondos, el que sirvió de base para el montaje de la obra fue el Archivo Histórico De La Provincia De Tunja, que recoge la documentación de buena parte de la Nueva Granada de la época Colonial y que conservó por espacio de 80 años la Academia Boyacense de Historia. Con el paso del tiempo y el desarrollo de la investigación histórica que exigía la apertura al público de los archivos históricos, se impuso la necesidad de crear un nuevo centro que facilitara el acceso a la información.</p>
                                <p>La creación del Archivo Histórico Regional de Boyacá, ha dado los resultados deseados y ha venido cumpliendo con su misión, puesto que se colocaron al servicio de la investigación diferentes fondos documentales de varias instituciones que ofrecen documentación prolija en información como la de las Notarías, la Gobernación, de archivos particulares y de empresas.</p>
                                <p>El gestor de la idea Dr. Jorge Palacios Preciado buscó un local que albergara la documentación y fue así como con la reconstrucción del Claustro de San Agustín de la ciudad de Tunja, logró la adecuación de los depósitos y de las salas pertinentes para la obra respectiva. Así, el Archivo Regional recogió y sigue recogiendo la documentación histórica de entidades distintas, contando hoy día con los siguientes Fondos y Secciones:</p>
                                <p>Fondo Archivo Histórico de Tunja; Fondo Libros del Cabildo de Tunja; Fondo Notarial con tres secciones: Sección Notaria Primera y Sección Notaría Segunda, Sección Notaría de Villa de Leiva; Fondo José Manuel Restrepo (rollos de microfilmes); Fondo Caciques e Indios (rollos de microfilmes); Fondo Militar; Fondo Eclesiástico; Fondo Gobernación con Secciones de Secretaría de Hacienda, Secretaría General y Secretaría de Gobierno; Fondo Rentas Departamentales y Empresa de Teléfonos de Boyacá. La hemeroteca con: El Boyacense, el Diario Oficial, la Gaceta de Colombia y algunos ejemplares de revistas y otros periódicos.</p>
                            </div> 
                            </div>
                            <h2>Fondo Archivo Historico De Tunja (1539-1860)</h2>
                            <div class="bs-blog-text" style="text-align: justify;"> 
                                <p>Posee documentación valiosa desde el Acta De Fundación De Tunja y otras ciudades del departamento de Boyacá. Allí se encuentran otros tipos de documentales como: Cédulas Reales, Provisionales Reales, Bulas Papales, Emplazamientos, Títulos Nobiliarios, Poderes, Probanza de Nobleza, Mortuorias, Inventarios, Dotes, Peticiones varias, Juicios civiles y criminales, Informes de Funcionarios, Órdenes, Apelaciones, Ejecutorias, Ternas para Alcaldes, Fundaciones de Capellanas, Testamentos, Tutorías, Remates y otros; este fondo se encuentra microfilmado.</p>
                             </div> 
                            </div>
                            <h2>Fondo Libros de Cabildo (1539-1810)</h2>
                            <div class="bs-blog-text" style="text-align: justify;"> 
                                <p>Encontramos: Provisiones de Tierras, haciendas y solares: Disposiciones sobre establecimientos de escuelas, temporalidades de regulares expatriados, expulsión de jesuitas, avalúo de bienes, arreglo de calles, servicios públicos, contratos de obra en iglesias, nacimiento de un príncipe, acceso al trono de un rey, matrimonio de personajes, muerte y funerales, Corpus Cristi, Semana Santa, Fiestas patronales, Permisos para juegos de bolos, toros, Encomiendas, Fundación de capellanías, Nombramientos, licencias y permisos a funcionarios públicos, cuentas, Donaciones, Tutorías, etc. Este fondo se encuentra microfilmado.</p>
                                <p>El Fondo se encuentra indizado y con sus respectivos catálogos gracias al Convenio del Archivo Regional de Boyacá con el Corpes Centro Oriente (contiene 23 volúmenes) También su contenido está en CD-ROM.</p>
                             </div> 
                            <h2>Fondos Archivos Protocolos Notariales (1540-1969)</h2>
                            <div class="bs-blog-text" style="text-align: justify;"> 
                                <p>Consta de tres secciones:</p>
                                <p>1. Notaria Primera (1540-1968), esta sección se encuentra microfilmada.</p>
                                <p>2. Notaria Segunda (1549-1969), esta sección se encuentra el 80% microfilmada.</p>
                                <p>3. Notaria de Villa de Leiva (1570-1899), esta sección se encuentra microfilmada.</p>
                                <p>Se pueden encontrar: Testamentos, Fundación de capellanías, venta y compra de esclavos, cofradías, Juicios de sucesión, poderes, Mortuorias, venta de tierras, animales y objetos, Hijuelas, Hipotecas, Resguardos (Sora, Cucaita, Sotaquira, Cómbita, Motavita, Siachoque, Chivatá, Viracachá, Villa de Leiva), Donaciones, Tierras, entre otros.</p>
                            </div> 
                            <h2>Fondo Archivo Eclesiastico (1620-1920)</h2>
                            <div class="bs-blog-text" style="text-align: justify;"> 
                                <p>Se refiere especialmente a documentos sobre Parroquias encontramos entre otros. fábrica de iglesias, Inventario de bienes eclesiásticos de diferentes pueblos de Boyacá, Censos de población, Número de cuadras en Tunja y otras poblaciones de Boyacá, Dispensas matrimoniales, Registro de bautismos, traslado de Sacerdotes y Obispos, Correspondencia del Obispado, Fundación de Parroquias, entre otros.</p>
                             </div> 
                            <h2>Fondo Archivo gobernacion-Hacienda (1921-1940)</h2>
                            <div class="bs-blog-text" style="text-align: justify;"> 
                                <p>Contiene Contratos de construcción de carreteras y puentes, rendición de cuenta de la renta del aguardiente, cuentas de las diferentes dependencias de Gobernación de Boyacá. Presenta secciones: Despacho del Gobernador, Secretario de Gobierno y Secretario General. </p>
                                <p>Como dependencias de la Secretaria de Hacienda encontramos:</p>
                                <ul>
                                    <li>Rentas de Boyacá. (1957-1988) Destinada a proteger los recursos del departamento, su labor principal el Recaudo de impuestos por aguardiente, cigarrillo, degüello de ganado. Encontramos archivo sobre recaudo de rentas en Sogamoso, Tunja, y de más municipios de Boyacá, documentos judiciales, recaudos de las Empresas de Bavaria, de Tabaco y otros.</li>
                                    <li>Secretaria de Gobierno, (1953-100) Encontramos acuerdos, contratos, informes, actas, correspondencia, decretos, resoluciones, solicitudes de los diferentes municipios del departamento de Boyacá.</li>
                                </ul>
                             </div> 
                            <h2>Fondo Archivo Militar (1818-1848)</h2>
                            <div class="bs-blog-text" style="text-align: justify;"> 
                                <p>Compuesto por correspondencia del Ejército, con temas de Permisos Militares, Permisos a Soldados, ordenes, Informes sobre situaciones de Boyacá en 1819 y otros sobre Colombia y Venezuela.</p>
                             </div> 
                            <h2>Fondo Archivo Caciques e Indios (1543-1820)</h2>
                            <div class="bs-blog-text" style="text-align: justify;"> 
                                <p>Conformado por 78 rollos de microfilm, copia cedida por el Archivo General de la Nación de Colombia, contiene temática sobre indígenas de la Región.</p>
                             </div> 
                            <h2>Fondo Archivo José Manuel Restrepo (1746-1931)</h2>
                            <div class="bs-blog-text" style="text-align: justify;"> 
                                <p>(1746-1931) Cedido por el Archivo General de la Nación en Microfilm. Contiene documentos importantes para el estudio de la historia de Colombia alrededor de este personaje en los primeros años de la República.</p>
                             </div> 
                            <h2>Fondo Archivo Empresa de Teléfonos de Boyacá (1921-1977)</h2>
                            <div class="bs-blog-text" style="text-align: justify;"> 
                                <p>Contiene archivo de la empresa de comunicaciones que funcionó en el Departamento al servicio de los diferentes municipios de la Región</p>
                                <p>Como tipología documental se encuentran: Resoluciones, correspondencia, proyectos, traslados telefónicos, contabilidad, presupuestos, cuentas, hojas de vida de funcionarios, certificados, informes de los municipios, cotizaciones, pedidos, facturación, inventarios, actas, acuerdos, estatutos entre otros. Es una documentación que sirve para adelantar trabajos en aspectos económicos, empresariales y de comunicaciones en Boyacá, en aquel período de la historia del Departamental.</p>
                             </div> 
                            <h2>Hemeroteca</h2>
                            <div class="bs-blog-text" style="text-align: justify;"> 
                                <p>Cuenta con algunos periódicos y revistas:</p>
                                <ul>
                                    <li>Periódico El Boyacense (1882-1966), como órgano oficial del Departamento.</li>
                                    <li>Gaceta de Colombia (1821-1831) Periódico de la República de Colombia.</li>
                                    <li>Diario Oficial. (1876-1994) Periódico Oficial de la República de Colombia.</li>
                                    <li>Revista Cultura. (1950-1973) Revista de extensión cultural popular de la Secretaria de Educación, hoy Instituto de Cultura de Boyacá.</li>
                                    <li>Revista Repertorio Boyacense (1913-2002), órgano oficial de la Academia Boyacense de Historia.</li>
                                    <li>Encontramos algunos ejemplares de les periódicos: Intermedio, El Vigía, El Siglo, El Mundo, Veritas, El Cruzado, El Trabajo, El Demócrata, Sábado, La Tierra. Senderos.</li>
                                    <li>Periódico El Informativo Boyacense (1972-1992) Contiene aspectos de la política regional.</li>
                                </ul>
